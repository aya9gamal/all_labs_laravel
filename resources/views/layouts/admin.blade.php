  <script type="text/javascript">
    $(document).ready(function() {
      // init datatable.
      var dataTable = $('.datatable').DataTable({
        processing: true,
        serverSide: true,
        autoWidth: false,
        pageLength: 5,
        // scrollX: true,
        "order": [
          [0, "desc"]
        ],
        ajax: '{{route('get-rooms')}}',
        columns: [{
            data: 'room_number',
            name: 'room_number'
          },
          {
            data: 'capacity',
            name: 'capacity'
          },
          {
            data: 'price',
            name: 'price'
          },
          {
            data: 'Actions',
            name: 'Actions',
            orderable: false,
            serachable: false,
            sClass: 'text-center'
          },
        ]
      });
      var id;
      $('body').on('click', '#rent', function(e) {
        $('.alert-danger').html('');
        $('.alert-danger').hide();
        id = $(this).data('id');
        $.ajax({
          method: 'GET',
          success: function(result) {
            console.log(result);
            $('#rentModalBody').html(result.html);
            $('#rentModal').show();
          }
        });
      });

      // Create article Ajax request.
      $('#confirmRent').click(function(e) {
       
        e.preventDefault();
        $.ajaxSetup({
          headers: {
            'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
          }
        });
        $.ajax({
          url: "{{ route('reservations.store') }}",
          method: 'post',
          data: {
            accompanies: $('#accompanies').val(),
            id: id,
          },
          success: function(result) {
            if (result.errors) {
              $('.alert-danger').html('');
              $.each(result.errors, function(key, value) {
                $('.alert-danger').show();
                $('.alert-danger').append('<strong><li>' + value + '</li></strong>');
              });
            } else {
              $('.alert-danger').hide();
              $('.alert-success').show();
              $('.datatable').DataTable().ajax.reload();
              setInterval(function() {
                $('.alert-success').hide();
                $('#rentModal').modal('hide');
                location.replace("{{route('reservations.index')}}");
              }, 2000);
            }
          }
        });
      });
      $('.modalClose').on('click', function() {
        $('#rentModal').hide();
      });

    });
  </script>

